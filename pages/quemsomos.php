 	Quem Somos?
	Oportunidade e muito trabalho são as razões do sucesso da fábrica de doces.<br>
A fábrica de doces começou seus trabalhos em 1991 em um imóvel de 20 m² em Balneário Camboriú, litoral de Santa Catarina. <br>
Fruto do sonho de seu Fundador, que buscava um nicho comercial ainda pouco explorado, para oferecer excelentes produtos aos seus clientes.<br>
Ao longo de sua carreira na área de vendas de produtos alimentícios, ele percebeu que, quanto ao fornecimento de produtos
relacionados à doceria tradicional brasileira, tais produtos eram desenvolvidos e oferecidos ao consumidor,
com pouquíssimo cuidado e profissionalismo, por empresas pouco estruturadas e sem um verdadeiro compromisso de qualidade.<br>
Ele então, passou a viajar por todo o país, em busca das melhores receitas de doces tradicionais brasileiros,
pesquisando a sua história, seus ingredientes, seus temperos secretos, suas melhores formas de produção e quais as melhores matérias primas usadas para a confecção desses produtos.<br>
Essa busca e pesquisa incessante cristalizaram-se e deu forma à Empresa Fábrica de doces. Hoje, a Empresa comercializa mais de 10 diferentes produtos na linha de doces tradicionais brasileiros.<br>
Toda a linha de Produtos da Fábrica, foi escolhida à dedo, através dos maiores critérios de: Qualidade, Procedência das matérias primas,
Idoneidade dos fabricantes e as melhores receitas já criadas, para poder levar à você consumidor, apenas o que há de melhor no segmento, tendo em vista,
um custo/benefício eficiente.<br>
Ao longo destes mais de trinta anos de trabalho, procuramos levar à você consumidor, o que existe de melhor da tradicional doceria brasileira,
e estamos pesquisando incessantemente para lhe trazer novos produtos, pois a nossa cozinha é muito rica em doces,
e existem muitas receitas à serem resgatadas de nossa história. <br>Receitas e segredos que passaram de Mãe para filha, ao longo de centenas de anos!
